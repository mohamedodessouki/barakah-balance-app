export type TranslationKey = keyof typeof translations.en;

export function useTranslation(language: 'en' | 'ar' | null) {
  const lang = language ?? 'en';
  return {
    t: (key: TranslationKey): string => {
      return translations[lang][key] ?? key;
    },
    isRTL: lang === 'ar',
  };
}
